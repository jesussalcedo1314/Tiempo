
package model;

public class datos {
    
    private String nombre;
    private String clase;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getClase() {
        return clase;
    }

    public void setClase(String clase) {
        this.clase = clase;
    }

    public datos(String nombre, String clase) {
        this.nombre = nombre;
        this.clase = clase;
    }

    
    
}
